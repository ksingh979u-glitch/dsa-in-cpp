#include<iostream>
using namespace std;
int main(){
//integer(whole number)
int a=20;
//double (number with decimal point)
double b=20.5;
//character(single character in single quotes)
char c='A';
//string(sequence of characters in double quotes)
string d="Hello, World!";
//boolean (true or false)
bool e=true;
cout<<"Integer: "<<a<<endl;
cout<<"Double: "<<b<<endl;
cout<<"Character: "<<c<<endl;
cout<<"String: "<<d<<endl;
cout<<"Boolean: "<<e<<endl;
return 0;
}